#!/usr/bin/env bash

###############################################################################
# Bacterial Isolate WGS Pipeline
# Utility Functions
###############################################################################

set -euo pipefail

###############################################################################
# Timestamp
###############################################################################

timestamp() {

    date +"%Y-%m-%d %H:%M:%S"

}

###############################################################################
# Runtime
###############################################################################

runtime() {

    local START=$1
    local END=$2

    local ELAPSED=$((END-START))

    local HOURS=$((ELAPSED/3600))
    local MINUTES=$(((ELAPSED%3600)/60))
    local SECONDS=$((ELAPSED%60))

    printf "%02dh %02dm %02ds" \
        "$HOURS" \
        "$MINUTES" \
        "$SECONDS"

}

###############################################################################
# Logger
###############################################################################

LOG_FILE=""

initialize_logger() {

    local LOG_DIRECTORY="$1"

    mkdir -p "${LOG_DIRECTORY}"

    LOG_FILE="${LOG_DIRECTORY}/pipeline_$(date +"%Y%m%d_%H%M%S").log"

    touch "${LOG_FILE}"

    exec > >(tee -a "${LOG_FILE}") 2>&1

    echo
    echo "=============================================================="
    echo "Bacterial Isolate WGS Pipeline"
    echo "Started: $(timestamp)"
    echo "Log file: ${LOG_FILE}"
    echo "=============================================================="
    echo

}

###############################################################################
# Logging
###############################################################################

log_info() {

    echo "$(timestamp) [INFO] $1"

}

log_success() {

    echo "$(timestamp) [SUCCESS] $1"

}

log_warning() {

    echo "$(timestamp) [WARNING] $1"

}

log_error() {

    echo "$(timestamp) [ERROR] $1"

}

###############################################################################
# Section Header
###############################################################################

print_header() {

    local TITLE="$1"

    echo
    echo "=============================================================="
    echo "${TITLE}"
    echo "=============================================================="

}

###############################################################################
# Directory Management
###############################################################################

create_directory() {

    mkdir -p "$@"

}

###############################################################################
# Output Checks
###############################################################################

check_output() {

    [[ -s "$1" ]]

}

###############################################################################
# Step Skipping
###############################################################################

skip_step() {

    local STEP="$1"
    local OUTPUT="$2"

    if check_output "${OUTPUT}"
    then
        log_warning "${STEP} already completed."
        log_info "Existing output detected: ${OUTPUT}"
        return 0
    fi

    return 1

}

###############################################################################
# Tool Checks
###############################################################################

check_tool() {

    local TOOL="$1"

    if ! command -v "${TOOL}" >/dev/null 2>&1
    then
        log_error "Required tool '${TOOL}' not found in PATH."
        exit 1
    fi

}

###############################################################################
# File Checks
###############################################################################

check_file() {

    local FILE="$1"

    if [[ ! -s "${FILE}" ]]
    then
        log_error "Required file not found: ${FILE}"
        exit 1
    fi

}

###############################################################################
# Directory Checks
###############################################################################

check_directory() {

    local DIR="$1"

    if [[ ! -d "${DIR}" ]]
    then
        log_error "Directory not found: ${DIR}"
        exit 1
    fi

}

###############################################################################
# Command Execution
###############################################################################

run_command() {

    log_info "Executing: $*"

    if ! "$@"
    then
        log_error "Command failed."
        exit 1
    fi

}

###############################################################################
# Step Runtime
###############################################################################

report_runtime() {

    local STEP="$1"
    local START="$2"

    local END=$(date +%s)

    log_success "${STEP} completed in $(runtime "$START" "$END")"

}

###############################################################################
# Pipeline Completion
###############################################################################

pipeline_complete() {

    echo

    echo "=============================================================="

    log_success "Pipeline completed successfully."

    log_info "Finished: $(timestamp)"

    echo "=============================================================="

    echo

}
